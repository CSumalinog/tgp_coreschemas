-- Governance tables for duty scheduling: publication snapshots, blackout dates, and audit trail.

create table if not exists public.duty_schedule_publications (
  id bigint generated by default as identity primary key,
  semester_id uuid not null references public.semesters(id) on delete cascade,
  published_by uuid references public.profiles(id) on delete set null,
  version integer not null,
  snapshot jsonb not null,
  created_at timestamptz not null default now(),
  published_at timestamptz not null default now(),
  unique (semester_id, version)
);

create index if not exists idx_duty_schedule_publications_semester
  on public.duty_schedule_publications (semester_id, version desc);

create table if not exists public.duty_schedule_blackout_dates (
  id bigint generated by default as identity primary key,
  semester_id uuid not null references public.semesters(id) on delete cascade,
  blackout_date date not null,
  reason text,
  created_by uuid references public.profiles(id) on delete set null,
  created_at timestamptz not null default now(),
  unique (semester_id, blackout_date)
);

create index if not exists idx_duty_schedule_blackout_dates_semester_date
  on public.duty_schedule_blackout_dates (semester_id, blackout_date);

create table if not exists public.duty_schedule_audit_logs (
  id bigint generated by default as identity primary key,
  semester_id uuid not null references public.semesters(id) on delete cascade,
  actor_id uuid references public.profiles(id) on delete set null,
  target_staffer_id uuid references public.profiles(id) on delete set null,
  request_id uuid references public.duty_schedule_change_requests(id) on delete set null,
  action_type text not null,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);

create index if not exists idx_duty_schedule_audit_logs_semester
  on public.duty_schedule_audit_logs (semester_id, created_at desc);

create index if not exists idx_duty_schedule_audit_logs_target
  on public.duty_schedule_audit_logs (target_staffer_id, created_at desc);

-- Repair path for environments where this table may already exist with
-- request_id typed incorrectly (e.g. bigint from an early draft).
do $$
declare
  request_col_type text;
  has_request_fk boolean;
begin
  if to_regclass('public.duty_schedule_audit_logs') is null then
    return;
  end if;

  select data_type
    into request_col_type
  from information_schema.columns
  where table_schema = 'public'
    and table_name = 'duty_schedule_audit_logs'
    and column_name = 'request_id';

  if request_col_type is not null and request_col_type <> 'uuid' then
    alter table public.duty_schedule_audit_logs
      drop constraint if exists duty_schedule_audit_logs_request_id_fkey;

    -- Existing non-uuid values cannot map to request UUIDs, so coerce to null.
    alter table public.duty_schedule_audit_logs
      alter column request_id type uuid using null;
  end if;

  select exists (
    select 1
    from pg_constraint
    where conname = 'duty_schedule_audit_logs_request_id_fkey'
      and conrelid = 'public.duty_schedule_audit_logs'::regclass
  ) into has_request_fk;

  if not has_request_fk then
    alter table public.duty_schedule_audit_logs
      add constraint duty_schedule_audit_logs_request_id_fkey
      foreign key (request_id)
      references public.duty_schedule_change_requests(id)
      on delete set null;
  end if;
end
$$;

