  on public.duty_schedule_publications
  for select
  using (auth.role() = 'authenticated');

drop policy if exists "Duty publications writable by admins" on public.duty_schedule_publications;
create policy "Duty publications writable by admins"
  on public.duty_schedule_publications
  for all
  using (
    exists (
      select 1
      from public.profiles p
      where p.id = auth.uid()
        and p.role = 'admin'
    )
  )
  with check (
    exists (
      select 1
      from public.profiles p
      where p.id = auth.uid()
        and p.role = 'admin'
    )
  );

